                    Nonostante l'intento iniziale, il Pallone d'oro FIFA non ricevette tuttavia un'accoglienza positiva, criticato da vari addetti ai lavori 
                    per le modalità attraverso cui si arrivava alla scelta del vincitore (vale a dire unendo ai voti dei giornalisti sportivi, come tradizionalmente 
                    avveniva per il Pallone d'oro, quelli degli allenatori e capitani delle nazionali, ereditati dal FIFA World Player of the Year), percepite come 
                    più attente al marketing e al richiamo mediatico piuttosto che agli effettivi valori sportivi espressi dai candidati durante l'anno. 
                    In tal senso, già nel 2011 la confederazione europea promosse un nuovo riconoscimento, l'UEFA Best Player in Europe Award, con l'intento 
                    di rilanciare lo spirito originario del Pallone d'oro.
                    Nel 2016 France Football pose quindi fine alla collaborazione con la FIFA, rispolverando l'originaria formula del Pallone d'oro 
                    di stampo unicamente giornalistico. Similmente, la federazione internazionale tornò sui suoi passi dando vita al nuovo The Best 
                    FIFA Men's Player, erede del FIFA World Player of the Year. Tra il 2018 e il 2019 la rivista francese istituì ulteriori premi accessori 
                    al Pallone d'oro: il Pallone d'oro femminile riservato alla migliore calciatrice del mondo, il Trofeo Kopa dedicato al miglior giocatore 
                    Under-21, e il Trofeo Jašin riservato al migliore portiere del mondo.
