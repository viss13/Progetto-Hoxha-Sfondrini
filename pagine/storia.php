                <h2>Dall'apertura extraeuropea alla contrapposizione con la FIFA</h2>
                <p class="spaziatura_p border-bottom-storia">
                    L'edizione 1995 segnò un importante punto di svolta per il Pallone d'oro, quando cadde la discriminante della nazionalità europea, 
                    aprendo quindi l'accesso al riconoscimento anche ai giocatori extraeuropei. Tale novità si riflesse immediatamente nell'albo d'oro: 
                    proprio in quell'anno il liberiano George Weah del Milan fu il primo e fin qui unico rappresentante dell'Africa a essere insignito del premio, 
                    mentre l'edizione 1997 vide il primo trionfo ufficiale del Sudamerica (escludendo i succitati precedenti degli oriundi Di Stéfano e Sívori) 
                    con il brasiliano Ronaldo dell'Inter. Tuttavia, ancora fino all'edizione 2006 il Pallone d'oro era riservato esclusivamente a calciatori 
                    militanti in club associati all'UEFA; dall'edizione 2007 anche questo ultimo paletto è caduto, potendo oggi concorrere al titolo gli sportivi 
                    di qualsiasi club affiliato alla FIFA.
                    In questi anni, tuttavia, si fece sempre più aspra la contrapposizione mediatica tra il Pallone d'oro di France Football, 
                    premio rimasto sempre di stampo giornalistico, e il FIFA World Player of the Year, riconoscimento nel frattempo nato nel 1991 per volontà della 
                    FIFA con l'intento di dare un carattere di ufficialità al titolo di miglior calciatore dell'anno, attraverso i voti dei commissari tecnici 
                    e dei capitani di ciascuna nazionale di calcio, nonché dei rappresentanti del FIFPro. Nonostante gli sforzi della federazione internazionale, 
                    tuttavia, nell'immaginario collettivo il Pallone d'oro continuò a essere considerato il premio di riferimento in questo ambito.
                </p>

                <h2>Dal Pallone d'oro FIFA al ritorno alle origini</h2>

                <p class="spaziatura_p">
                    Per tentare di porre fine al succitato dualismo mediatico, nel 2010 i due riconoscimenti si fusero dando vita a un nuovo premio, il Pallone 
                    d'oro FIFA, organizzato congiuntamente da France Football e dalla FIFA. Pur se il Pallone d'oro e il Pallone d'oro FIFA rimangono de iure 
                    due trofei diversi, in questo periodo de facto sia i due istituti assegnatari sia i media considerarono un unico palmarès tra i 
                    due riconoscimenti. In tal senso, all'inizio del XXI secolo emersero nell'albo d'oro Lionel Messi e Cristiano Ronaldo, vincitori 
                    rispettivamente per 8 e 5 edizioni (4 Palloni d'oro e 4 Palloni d'oro FIFA per l'argentino, 3 e 2 per il portoghese); Messi detiene 
